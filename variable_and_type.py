# python中最基本的四種資料型別
# int 整數型別用於表示沒有小數部分的數字。
age=20
grade=3
year = 2024

# float 浮點數用於表示帶有小數部分的數字，能夠精確表示實數。
height = 183.9
pi = 3.14159

# str 字串用於表示文字資料，它們由一對引號括起來。
name = "邵威捷"
greeting = '你好，我是大三資管系的學生，興趣是研究程式，最近開始接觸股票投資'

#bool 布林值表示真或假的邏輯值，在程式中常用於條件判斷。布林值有兩個值：True（真）和 False（假）。
is_student = True
has_graduated = False


# 型別轉換
name="jayshao"
age=20
gpa=4.3
student=True

# 顯示型別轉換
age=float(age)
print(type(age))
gpa=int(gpa)
student=str(student)

# 隱示型別轉換
x=10
y=5.0
x=x/y
print(type(x))




